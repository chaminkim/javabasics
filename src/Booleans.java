public class Booleans {
    public static void main(String[] args) {
        boolean isHotToday = true;
        System.out.println(isHotToday);

        int a = 10;
        int b = 9;
        System.out.println(a > b);
        System.out.println(a == b);

        String c = "10";
        System.out.println(String.valueOf(a).equals(c));

    }
}
